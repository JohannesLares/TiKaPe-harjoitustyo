package tikape;

public class Annos {
	private String nimi;
	private int id;
	
	public Annos(String nimi, int id) {
		this.nimi = nimi;
		this.id = id;
	}

	public String getNimi() {
		return nimi;
	}

	public void setNimi(String nimi) {
		this.nimi = nimi;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}
	
	
}
